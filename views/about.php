					<h4>Hello</h4>
					<p>I am a doctoral candidate and NSF Fellow in the <a href="http://ucjeps.berkeley.edu/Baldwin-Lab.html">Baldwin Lab</a> at UC Berkeley. 
					My research interests revolve around empirical and theoretical aspects of 
					plant evolutionary genomics, computational phylogenetics, and biodiversity informatics.</p> 
                    <!-- Evolutionary Genomics and Bioinformatics -->
					<h4>Plant evolutionary genomics</h4>
					<p>
					Currently, I'm using phylogenomic approaches to study reticulate evolution in <em>Chylismia</em> (Onagraceae).
                    I'm applying <em>Chylismia</em> data and simulations to test a composite likelihood approach I've developed
                    that infers ancient introgression and hybridization events over phylogenies using coalescent probabilities.
					</p>
					<h4>Computational phylogenetics and modeling</h4>
					<p>
                    I've worked on implementing ancestral state reconstruction functionality in the 
                    <a href="https://github.com/revbayes/revbayes/wiki">RevBayes</a> project. In RevBayes I've also developed
                    continuous-time Markov chain models of discrete character evolution, in particular chromosome number evolution.
                    I'm broadly interested in mathematical models of evolution and applying them with Bayesian inference.
					</p>
					<h4>Biodiversity informatics</h4>
					<p>At UC Berkeley I've been part of the <a href="http://ucjeps.berkeley.edu/bryolab/CPPP/">California Phylodiversity Project</a>.
                    Previously, working with non-profit and volunteer conservation groups, 
                    I developed open source web-based tools to help manage and assess conservation projects.
					I'm interested in building tools that visualize biodiversity data and help make conservation management 
					more collaborative and transparent. Can we build useful tools for conservationists that incorporate metrics of 
					phylogenetic diversity?</p>
