					<h3>Hello</h3>
					<p>I am a postdoctoral researcher in the <a href="http://eeg.github.io/lab/">Goldberg Lab</a> at UMN. 
                    In September 2018 I join the research team at <a href="http://23andme.com">23andMe</a> as a population geneticist.
					My research interests revolve around empirical and theoretical aspects of 
					computational phylogenetics and evolutionary genomics.</p> 
					<h4><b>Computational phylogenetics and modeling</b></h4>
					<p>
                    I've implemented ancestral state reconstruction and stochastic character mapping functionality in the 
                    <a href="http://revbayes.com">RevBayes</a> project. In RevBayes I've also worked on
                    state-dependent speciation and extinction models of discrete character evolution.
                    I'm broadly interested in mathematical models of evolution and applying them with Bayesian inference.
					</p>
                    <!-- Evolutionary Genomics and Bioinformatics -->
					<h4><b>Evolutionary genomics</b></h4>
					<p>
                    Introgression and hybridization are increasingly understood to be important evolutionary processes across the tree of life.
                    I work on models that bridge coalescent and phylogenetic theory to infer ancient introgression and hybridization events.
					</p>
