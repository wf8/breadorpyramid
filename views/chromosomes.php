						<h4>Cladogenetic and anagenetic models of chromosome number evolution: model averaging using reversible jump MCMC</h4>
						<p>
                        The fundamental role that chromosomal changes play in evolution was confirmed
                         in Dobzhansky's groundbreaking <em>Genetics and the Origin of Species</em> 
                         (1937), which unified Mendelian genetics and Darwinian natural selection into a
                         single coherent theory of evolution.
                         "Chromosomal changes are one of the mainsprings of
                         evolution," Dobzhansky asserted, and chromosomal rearrangements such as
                         deletions, duplications, inversions, and translocations could help explain the
                         formation of reproductive isolation and lineage diversification.
                            The recent development of probabilistic models of chromosome evolution in the 
                            groundbreaking work by Mayrose et al. (2010)
                            have enabled the inference of ancestral chromosome numbers over molecular phylogenies and 
                            generated new interest in studying the role of chromosome changes in evolution.
                            However, these models assume all changes occur anagenetically (along branches),
                            and do not model events that are specifically cladogenetic and may be expected if chromosomal
                            changes result in reproductive isolation.
                        </p><p>
                            In this research I've developed a class of models of chromosome number evolution
                            that incorporate both anagenetic and cladogenetic change.
                            The motivation in developing these new models 
                            is to determine the mode of chromosome number evolution; is
                            chromosome evolution occurring primarily within lineages, primarily at lineage
                            splitting, or in clade-specific combinations of both? By identifying how much of the
                            pattern of chromosome number evolution is explained by anagenetic versus
                            cladogenetic change, we can help uncover how much of a role chromosomal
                            changes play in speciation.
						</p>
