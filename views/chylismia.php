						<h4>The genomic architecture of reticulate evolution: phylogenomics and hybridization in <em>Chylismia</em> (Onagraceae)</h4>
						<p>
Increasingly, hybridization is understood to be an important evolutionary process in a substantial portion of many plant and animal groups. 
One explanation for the extent of hybridization can be found in G. Ledyard Stebbins’ 1959 hypothesis that hybridization acts as an evolutionary catalyst 
by increasing the adaptive variation within lineages. 
The plant genus <em>Chylismia</em> (Onagraceae) is an ideal study group to empirically test this evolutionary hypothesis, 
based on the strong foundation of cytogenetic, biosystematic, and descriptive data detailed in Peter Raven’s monograph. 
<em>Chylismia</em> consists of 35 taxa and includes a homogamic (diploid) hybrid complex of 14 species distributed throughout the harsh 
habitats of the Sonoran Desert and Great Basin of North America. 
Raven hypothesized that the members of <em>Chylismia</em> diverged during a rapid radiation resulting from the increasing aridity of the 
Great Basin region following the uplift of the Sierra Nevada. He further inferred that the complex of highly variable and hybridizing 
<em>Chylismia</em> species is the result of repeated vicariance and recontact due to pluvial lake cycles during the Pleistocene.
</p><p>
While phylogenetic trees are typically employed to test such evolutionary scenarios, their application is challenging when the evolutionary 
hypotheses being tested involve hybridization and other reticulate patterns. 
Rigorous testing of reticulate evolutionary hypotheses warrants both inferring phylogenetic networks and using comparative methods that work on phylogenetic networks. 
However many current phylogenetic network inference methods are limited in that they assume reticulate events are the 
result of only horizontal gene transfer and not hybridization, or they only handle evolutionary scenarios with a single hybridization event. 
Motivated both by Stebbins’ 1959 hypothesis and the challenges of phylogenetic network inference, my research incorporates 
both empirical and theoretical approaches: 
using next-generation sequencing and genomic datasets I am characterizing hybrid speciation and introgression in <em>Chylismia</em>, 
quantifing the accompanying genomic structural changes, and contributing to the development of new phylogenetic methods.
</p>
