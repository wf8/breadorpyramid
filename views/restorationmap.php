						<h4>Restoration Map</h4>
						<p><a href="http://restorationmap.org">Restoration Map</a> is an open-source and free web-based application to help plan, implement, and 
						assess ecological restoration projects within Chicago Wilderness natural areas.
						It is designed to encourage collaboration and improve communication
						among stakeholders including the landowner 
						agencies, volunteers, restoration contractors, interns, and partner conservation organizations.
						</p><p>
						The map's users have entered over 1800 map layers representing 13 years of management work 
						and experimentation in Chicago-area restoration projects.
						By integrating long term monitoring data from the Bird Conservation Network (eBird), the Calling Frog 
						Survey, and other sources, <a href="http://restorationmap.org">Restoration Map</a> provides feedback on the effects of restoration work, therefore 
						utilizing an adaptive management approach.
						</p><p>
						<a href="http://restorationmap.org">Restoration Map</a> has been developed with the support of the <a href="http://www.audubon.org/">National Audubon Society</a> using all free and 
						open-source software. The concept was inspired by Stephen Packard's stewardship of the beautiful 
						<a href="http://www.sommepreserve.org/">Somme Prairie Grove</a>.
						The map is built on PHP and MySQL, and uses the Google Earth plugin.</p>
						<p class="text-info">Learn more at <a href="http://restorationmap.org">http://restorationmap.org</a></p>
