						<h4>Phylogeny and divergence times among the North American <em>Rhodiola</em></h4>
						<p>
						Plants in the genus <em>Rhodiola</em> (Crassulaceae) are perennial herbs that generally grow in alpine and 
						arctic regions of Eurasia and North America. Some are used in traditional medicines, and some, 
						such as the North American <em>Rhodiola integrifolia</em> ssp. <em>leedyi</em>, (Leedy’s roseroot) are rare and 
						narrowly endemic. 
						</p><p>
						To test the evolutionary relationships of the North American <em>Rhodiola</em> taxa, 
						<a href="http://www.neiu.edu/~biology/">Dr. Joel Olfelt</a> and I analyzed the nucleotide 
						sequences of the nuclear ribosomal internal transcribed spacer (ITS) and three non-coding plastid 
						loci: the trnS-trnG intergenic spacer, the trnL intron and adjacent trnL-trnF spacer. We sequenced 
						these regions in over 60 individuals representing range-wide geographic sampling of the six named 
						North American <em>Rhodiola</em> taxa. 
						Combining our data with GenBank sequences from Asian <em>Rhodiola</em>, we 
						performed phylogenetic analyses using parsimony, maximum likelihood, and Bayesian methods. 
						Additionally, we estimated the divergence 
						times of <em>Rhodiola</em> based on the ITS data using a Bayesian relaxed clock model. 
						</p><p>Our analyses reveal 
						that two separate lineages of <em>Rhodiola</em> colonized North America an estimated 2.0 and 0.7 mya, and 
						that two well-supported clades within <em>R. integrifolia</em> diverged from each other an estimated 1.0 mya.  
						These findings suggest that glacial movements during the Pleistocene resulted in allopatric speciation 
						and that the lineage currently classified as <em>R. integrifolia</em> needs revision. Furthermore, a South 
						Dakota population previously identified as <em>R. integrifolia</em> ssp. <em>integrifolia</em> actually 
						belongs in <em>R. integrifolia</em> ssp. <em>leedyi</em>, so we recommend that the conservation measures 
						in place for Leedy’s roseroot populations be extended to the South Dakota population.
						</p>
