						<h4><b>Detecting introgression in the presence of incomplete lineage sorting: phylogenomics and hybridization in <em>Chylismia</em> (Onagraceae)</b></h4>
						<p>
Increasingly, hybridization is understood to be an important evolutionary process in a substantial portion of many plant and animal groups. 
One explanation for the extent of hybridization can be found in G. Ledyard Stebbins’ 1959 hypothesis that hybridization acts as an evolutionary catalyst 
by increasing the adaptive variation within lineages. 
However, most current phylogenetic models assume bifurcating trees and are inadequate to test 
hypotheses involving hybridization and other reticulate patterns.
To address this, I've developed a composite likelihood approach that estimates ancient introgression and hybridization events
over a phylogeny. The model combines elements of both coalescent theory and phylogenetics 
by calculating the joint posterior probability of a coalescent model of ABBA and BABA incongruent gene tree histories 
simultaneously with a phylogenetic molecular evolution model conditioned on a population tree.
</p><p>
The plant genus <em>Chylismia</em> (Onagraceae) is an ideal study group to empirically test these new models, 
based on the strong foundation of cytogenetic, biosystematic, and descriptive data detailed in Peter Raven’s monograph. 
<em>Chylismia</em> consists of 35 taxa and includes a homogamic (diploid) hybrid complex of 14 species distributed throughout the harsh 
habitats of the Sonoran Desert and Great Basin of North America. 
Raven hypothesized that the members of <em>Chylismia</em> diverged during a rapid radiation resulting from the increasing aridity of the 
Great Basin region following the uplift of the Sierra Nevada. He further inferred that the complex of highly variable and hybridizing 
<em>Chylismia</em> species is the result of repeated vicariance and recontact due to pluvial lake cycles during the Pleistocene.
</p><p>
Motivated both by the challenges of phylogenetic network inference and by Stebbins’ 1959 hypothesis of hybridization as a catalyst for evolution, this research incorporates both empirical and theoretical work: 
(1) I'm using next-generation sequencing and genomic datasets to study the evolutionary consequences of introgression and historical biogeography across the hybridizing <em>Chylismia</em> species, 
(2) applying both empirical <em>Chylismia</em> datasets and simulated datasets to evaluate the new model described above, and 
(3) quantifying the genomic structural changes that accompany reticulate evolution and their functional significance in <em>Chylismia</em> using whole chloroplast genomes and RAD markers from across the nuclear genome.
</p>
